// Todas as bibliotecas do c++
#include <bits/stdc++.h>


// Faz com que não seja necessário adicionar 'std' nos comandos
using namespace std;

// Executa tudo o que tá aqui dentro, função principal
int main() {

    // Declarando variável do tipo num
    int num;

    // Mostrando um texto para o usuário
    cout << "Digite um numero: ";

    // Guardando o que o usuário digitou na variável 'num'
    cin >> num;

    // Criando a variável resultado e somando o valor que o usuário digitou em 10 unidades
    int resultado = num + 10;

    // Imprimindo a variável resultado
    cout << resultado;
}